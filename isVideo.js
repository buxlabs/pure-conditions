function isVideo (string) {
  const extensions = [
    '.webm',
    '.mkv',
    '.flv',
    '.flv',
    '.vob',
    '.ogv',
    '.ogg',
    '.drc',
    '.gifv',
    '.mng',
    '.avi',
    '.mov',
    '.qt',
    '.wmv',
    '.yuv',
    '.rm',
    '.rmvb',
    '.asf',
    '.amv',
    '.mp4',
    '.m4v',
    '.mpg',
    '.mp2',
    '.mpeg',
    '.mpe',
    '.mpv',
    '.mpg',
    '.mpeg',
    '.m2v',
    '.m4v',
    '.svi',
    '.3gp',
    '.3g2',
    '.mxf',
    '.roq',
    '.nsv',
    '.flv',
    '.f4v',
    '.f4p',
    '.f4a',
    '.f4b'
  ]
  return extensions.includes(string.substr(string.lastIndexOf('.')))
}
module.exports = isVideo
