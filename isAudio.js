function isAudio (string) {
  const extensions = [
    '.3gp',
    '.aa',
    '.aac',
    '.aax',
    '.act',
    '.aiff',
    '.amr',
    '.ape',
    '.au',
    '.awb',
    '.dct',
    '.dss',
    '.dvf',
    '.flac',
    '.gsm',
    '.iklax',
    '.ivs',
    '.m4a',
    '.m4b',
    '.m4p',
    '.mmf',
    '.mp3',
    '.mpc',
    '.msv',
    '.nsf',
    '.ogg',
    '.oga',
    '.mogg',
    '.opus',
    '.ra',
    '.rm',
    '.raw',
    '.sln',
    '.tta',
    '.vox',
    '.wav',
    '.wma',
    '.wv',
    '.webm',
    '.8svx'
  ]
  return extensions.includes(string.substr(string.lastIndexOf('.')))
}
module.exports = isAudio
